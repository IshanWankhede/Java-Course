package com.ishan.javalearning.datatypes;

public class Main {
    public static void main(String[] args) {
        System.out.println("Hello World");
    }
}

/*
package → Organizes classes into folders/packages.
public class Main → Creates a class named Main.
public → Accessible from anywhere.
static → Can be called without creating an object.
void → Returns nothing.
main() → Starting method of every Java program.
String[] args → Stores command-line arguments.
System.out.println() → Prints output and moves to the next line.
 */